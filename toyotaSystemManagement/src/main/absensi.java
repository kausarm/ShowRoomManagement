    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
            // TODO add your handling code here:
            try{
                Connection con = (Connection)projecttoyota.config();
            String sql = "INSERT INTO kehadiran VALUES (?, ?, ?)";
            PreparedStatement pstm = con.prepareStatement(sql);
            
            pstm.setString(1, tanggal.getText());
            pstm.setString(2, id_karyawan.getText());
            
            String kehadiran;
            kehadiran = absen.getSelectedItem().toString();
            pstm.setString(3, kehadiran);
            
            pstm.execute();
            
            JOptionPane.showMessageDialog(null, "Berhasil Absen!", "INFORMASI", JOptionPane.INFORMATION_MESSAGE);
            tampilkan();
            kosong();
            
            } catch (SQLException e) {
             JOptionPane.showMessageDialog(null,e);
         }
    }//GEN-LAST:event_saveActionPerformed

