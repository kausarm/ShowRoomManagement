     private void tampilkan(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("no");
        model.addColumn("Tanggal");
        model.addColumn("ID Karyawan");
        model.addColumn("Nama");
        model.addColumn("Keterangan");
        try {
            int no = 1;
            String sql = "SELECT k.jadwal_kehadiran, k.id_karyawan, kr.nama, k.status_kehadiran FROM kehadiran k INNER JOIN karyawan kr ON k.id_karyawan=kr.id_karyawan";
            Connection con = (Connection)projecttoyota.config();
            Statement stm = con.createStatement();
            ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
            model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4)});
        }
            tabel_absensi.setModel(model);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }

