    private void jLabel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MouseClicked
        // TODO add your handling code here:
        int row=tableKaryawan.getRowCount();
        for(int a=0;a<row;a++){
            model.removeRow(0);
        }
        try {
            sql = "SELECT `id_karyawan`, `nama`, `jenis_kelamin`, `agama`, `pendidikan`, `no_telp`, `alamat`, karyawan.kd_jabatan`kd_jabatan`,`nama_jabatan` FROM `karyawan` INNER JOIN jabatann ON karyawan.kd_jabatan=jabatann.kd_jabatan WHERE nama='"+fieldSearch.getText()+"' OR id_karyawan='"+fieldSearch.getText()+"'";                           
            rs = stat.executeQuery(sql);
            while(rs.next()){
                String data[] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8),rs.getString(9)};
                model.addRow(data);
                }           
        } catch (SQLException ex) {
            Logger.getLogger(landingPage.class.getName()).log(Level.SEVERE,null,ex);
        }
    }//GEN-LAST:event_jLabel10MouseClicked

