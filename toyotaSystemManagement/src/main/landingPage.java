    private void mouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mouseClicked
        
        
        
        // TODO add your handling code here:

        if(evt.getSource()==employesLabel){
            cardKaryawan.setVisible(true);
            cardTransaksi.setVisible(false);
        }
        
         if(evt.getSource()== absensiLabel){
            new absensi().setVisible(true);
            this.dispose();
        }
         
          if(evt.getSource()==unitLabel){
            try {
                new Barang().setVisible(true);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(landingPage.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
           
            if(evt.getSource()==transaksiLabel){
            new transaksii().setVisible(true);
            this.dispose();
        }
            if(evt.getSource()==closeLabel){
            this.dispose();
        }
    }//GEN-LAST:event_mouseClicked

